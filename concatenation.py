#String Concatenation (combine or add two strings together)
a = "Hello"
b = "World"
c = a + b
print(c)

#Use " " for space between the two variables
e = a + " " + b
print(e)

#Format String
x = "Panadol"
y = "I use the medicine, {}"
print(y.format(x))

#Escape Character
d = "my answer is \'yes\'"
print(d)